$('#section-section2 #minuscule').click(function(){

    if($('.caracteres p').hasClass('minusculeActive')){

        $('.caracteres p').removeClass('minusculeActive');
        $(this).removeClass('boutonActive')
    }else{
        $(this).addClass('boutonActive')
        $('.caracteres p').removeClass('majusculeActive')
        $('.caracteres p').addClass('minusculeActive');
        $('#majuscule').removeClass('boutonActive')
    }
})

